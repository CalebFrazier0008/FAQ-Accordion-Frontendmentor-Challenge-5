function showHide1() {
  const questionNode = document.querySelector(".QuestionCon1");
  const answerNode = questionNode.children[1];
  const headerNode = document.querySelector(".Question-1");
  const plusNode = headerNode.children[0];
  const minusNode = headerNode.children[1];
  answerNode.classList.toggle("Hidden");
  plusNode.classList.toggle("Hidden");
  minusNode.classList.toggle("Hidden");
}
